   public void cargarPeliculas(){
        DefaultTableModel modelo = (DefaultTableModel) tblC.getModel();
        modelo.setRowCount(0);
        rs = conexion.ConexionBD.Consulta("SELECT * FROM Peliculas WHERE nom_Pelicula LIKE '"+txt1.getText()+"%'");
        try {
            while (rs.next()){
                Vector v= new Vector();
                v.add(rs.getInt(1));
                v.add(rs.getString(2));
                v.add(rs.getString(3));
                v.add(rs.getString(4));
                v.add(rs.getString(5));
                v.add(rs.getInt(6));
                modelo.addRow(v);
                tblC.setModel(modelo);
                
            }
        } catch (SQLException e) {
            
        }
    }
			
			

